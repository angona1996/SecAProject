package model;

public class StudentModel {
   private String name1= "A";
   private String name2="B";
    private String roll1="roll 1";
   private String roll2="roll 2";

    public StudentModel() {
    }

    public String getName1() {
        return name1;
    }

    public void setName1(String name1) {
        this.name1 = name1;
    }

    public String getName2() {
        return name2;
    }

    public void setName2(String name2) {
        this.name2 = name2;
    }

    public String getRoll1() {
        return roll1;
    }

    public void setRoll1(String roll1) {
        this.roll1 = roll1;
    }

    public String getRoll2() {
        return roll2;
    }

    public void setRoll2(String roll2) {
        this.roll2 = roll2;
    }
}
